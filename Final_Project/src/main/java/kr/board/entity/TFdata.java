package kr.board.entity;

public class TFdata {
	 // 신발 브랜드
	 private String f_brand;

	 // 신발 상품명
	 private String f_name;

	 // 신발가격
	 private String f_price;

	 // 신발 이미지 
	 private String f_img;

	 // 신발 사이즈 
	 private String f_size;
	 
	 // 신발 구분
	 private int f_cate;

	public String getF_brand() {
		return f_brand;
	}

	public void setF_brand(String f_brand) {
		this.f_brand = f_brand;
	}

	public String getF_name() {
		return f_name;
	}

	public void setF_name(String f_name) {
		this.f_name = f_name;
	}

	public String getF_price() {
		return f_price;
	}

	public void setF_price(String f_price) {
		this.f_price = f_price;
	}

	public String getF_img() {
		return f_img;
	}

	public void setF_img(String f_img) {
		this.f_img = f_img;
	}

	public String getF_size() {
		return f_size;
	}

	public void setF_size(String f_size) {
		this.f_size = f_size;
	}

	public int getF_cate() {
		return f_cate;
	}

	public void setF_cate(int f_cate) {
		this.f_cate = f_cate;
	}

	
}
